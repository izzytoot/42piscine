#include <unistd.h>
#include <stdio.h>

unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	unsigned int	i;
	unsigned int	len;

	i = 0;
	len = 0;
	while (src[len] != '\0')
	{
		len++;
	}
	while (src[i] != '\0' && i < size - 1)
	{
		dest[i] = src[i];
		i++;
	}
	if (size > 0)
	{
		dest[i] = '\0';
	}
	return (len);
}

int	main(void)
{
	char	src[] = "Hello!";
	char	dest[] = "";
	int	x;
	
	x = ft_strlcpy(dest, src, 5);
	
	printf("%s\n", dest);
	printf("%i\n", x);
}
